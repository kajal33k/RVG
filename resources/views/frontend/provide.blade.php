 <script>
     const slider = document.getElementById('achievementSlider');
     const sliderContainer = document.getElementById('achievementSliderContainer');
     const originalSlides = Array.from(slider.children);

     let offset = 0;
     let isTransitioning = false;
     let sliderInterval;

     // Clone slides to enable infinite loop effect
     originalSlides.forEach(slide => {
         const clonedSlide = slide.cloneNode(true);
         slider.appendChild(clonedSlide);
     });

     // Function to dynamically get slide width
     function getSlideWidth() {
         return slider.children[0].offsetWidth + 2 * 16; // Width + margins
     }

     // Function to move slider
     function slide() {
         if (isTransitioning) return;

         const slideWidth = getSlideWidth();
         isTransitioning = true;
         offset -= slideWidth;
         slider.style.transition = 'transform 0.5s ease-out';
         slider.style.transform = `translateX(${offset}px)`;

         setTimeout(() => {
             if (Math.abs(offset) >= slideWidth * originalSlides.length) {
                 slider.style.transition = 'none';
                 offset = 0;
                 slider.style.transform = `translateX(${offset}px)`;
             }
             isTransitioning = false;
         }, 500);
     }

     // Start auto slide
     function startSliding() {
         sliderInterval = setInterval(slide, 6000);
     }

     // Stop auto slide
     function stopSliding() {
         clearInterval(sliderInterval);
     }

     // Init on page load
     document.addEventListener('DOMContentLoaded', function() {
         startSliding();
         sliderContainer.addEventListener('mouseenter', stopSliding);
         sliderContainer.addEventListener('mouseleave', startSliding);
     });

     // Handle resize to recalculate offsets if screen size changes
     window.addEventListener('resize', () => {
         offset = 0;
         slider.style.transition = 'none';
         slider.style.transform = `translateX(${offset}px)`;
     });
 </script>
